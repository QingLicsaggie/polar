const unsigned char tab_crc4[16] = {0, 3, 6, 5, 12, 15, 10, 9, 11, 8, 13, 14, 7, 4, 1, 2 };

unsigned short crc4(int *buf,int length)
{
    register int k=0;
    register unsigned short crc=0;
    
    for(k=0; k < length;k++)
    {
        crc^=buf[k]>>4;       //PARTE ALTA
        crc=tab_crc4[crc];
        crc^=buf[k]&0xF;    //PARTE BAIXA
        crc=tab_crc4[crc];
    }
    
    return crc;
}


unsigned short crc8(int *data, int length){
    register unsigned short crc = 0;
    for (int i = 0; i < length; i++){
        crc = crc8table[data[i] ^ crc];
    }
    return crc;
}


unsigned short crc16(int *buf, int len)
{
    register int counter;
    register unsigned short crc = 0;
    for( counter = 0; counter < len; counter++)
        crc = (crc<<8) ^ crc16tab[((crc>>8) ^ *(int *)buf++)&0x00FF];
    return crc;
}